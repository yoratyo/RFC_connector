    private void initProperties(){
        destinationField.setText(properties.destination);
        getSOField.setText(properties.getFunctionName);
        sendSOField.setText(properties.postFunctionName);

        Properties prop = properties.getDestinationProperties();
        hostField.setText(prop.getProperty("jco.client.ashost"));
        sysnrField.setText(prop.getProperty("jco.client.sysnr"));
        clientField.setText(prop.getProperty("jco.client.client"));
        routerField.setText(prop.getProperty("jco.client.saprouter"));
        userField.setText(prop.getProperty("jco.client.user"));
        passwordField.setText(prop.getProperty("jco.client.passwd"));
    }

    private void initTask() {
        timer = new Timer(); // creating timer
        sap_connection = new sap_connection(destinationField.getText(), getDestinationProperties(), getSOField.getText(), sendSOField.getText());
        task1 = new get_task(sap_connection.getDestination(), sap_connection.getFunctionGet(), model); // creating get task
        task2 = new post_task(sap_connection.getDestination(), sap_connection.getFunctionPost(), model); // creating get task
    }

    private Properties getDestinationProperties() {
        //adapt parameters in order to configure a valid destination
        Properties connectProperties = new Properties();
        connectProperties.setProperty(DestinationDataProvider.JCO_ASHOST, hostField.getText()); //185 - 186
        connectProperties.setProperty(DestinationDataProvider.JCO_SYSNR, sysnrField.getText());
        connectProperties.setProperty(DestinationDataProvider.JCO_CLIENT, clientField.getText()); //381 - 280
        connectProperties.setProperty(DestinationDataProvider.JCO_USER, userField.getText()); //RFCWBRIDGE - WILLIAMP
        connectProperties.setProperty(DestinationDataProvider.JCO_PASSWD, String.valueOf(passwordField.getPassword())); //123456 - abcd1234
        connectProperties.setProperty(DestinationDataProvider.JCO_LANG, this.defaultLang);
        connectProperties.setProperty(DestinationDataProvider.JCO_SAPROUTER, routerField.getText()); //3299 - 3200
        return connectProperties;
    }

