    private void stopTask(){
        this.timer.cancel();
        this.timer.purge();
        this.runState = false;

        button.setText("  Run");
        button.setBackground(new java.awt.Color(103,242,209));
        button.setIcon(new javax.swing.ImageIcon(getClass().getResource("/submit.png")));
    }

    private void startTask(){
        button.setText("  Stop");
        button.setBackground(new java.awt.Color(255, 76, 48));
        button.setIcon(new javax.swing.ImageIcon(getClass().getResource("/exit.png")));

        Thread T = new Thread(() -> {
            initTask();
            this.timer.schedule(task1, 1000, 10000);
            this.timer.schedule(task2, 5000, 10000);
            this.runState = true;
        });
        T.start();
    }

    private void buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonMouseClicked
        if (this.runState) {
            stopTask();
        } else {
            startTask();
        }
        // scheduling the task for repeated fixed-delay execution, beginning after the specified delay
    }//GEN-LAST:event_buttonMouseClicked

